function handleSelectionEvent() {
    var selectBox = document.getElementById("selectbox");
    var selectedValue = selectBox.value;
    

    if (selectedValue === "all-event") {
        sort_all_event();
    } else if (selectedValue === "band") {
        sort_band();
    } else if (selectedValue === "dance") {
        sort_dance();
    } else if (selectedValue === "bukatu") {
        sort_bukatu();
    } else if (selectedValue === "owarai") {
        sort_owarai();
    } else if (selectedValue === "others") {
        sort_others();
    };
};

